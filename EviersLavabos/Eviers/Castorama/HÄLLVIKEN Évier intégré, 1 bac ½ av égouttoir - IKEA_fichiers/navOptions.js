function backimage() {
    "use strict";
    var str, endte, start, end, d, today, month, day;
    str = "2012-08-22";
    endte = "2012-08-22";
    start = str.replace(/-/g, "");
    end = endte.replace(/-/g, "");
    d = new Date();
    month = d.getMonth() + 1;
    day = d.getDate();
    today = d.getFullYear() +
        ((month.toString()).length < 2 ? '0' : "") + month +
        ((day.toString()).length < 2 ? '0' : "") + day;
    if (today >= start) {
        if (end >= today) {
                com.ikea.irw.GUI.background.changeBackground("url(/ms/img/homepage_background/bg01.jpg)");
        }
    }
}
